    static func countries() -> [AKCountry] {
        var countries = [AKCountry]()
        for index in 0...AKCountry.count.rawValue-1 {
            countries.append(AKCountry(rawValue: index)!)
        }
        return countries
    }
    
    func name() -> String? {
        return AKCountry.info[self]?[AKCountry.kName]
    }
    
    func fullName() -> String? {
        return AKCountry.info[self]?[AKCountry.kFullName]
    }
    
    func countryCode() -> String? {
        return AKCountry.info[self]?[AKCountry.kCountryCode]
    }
    
